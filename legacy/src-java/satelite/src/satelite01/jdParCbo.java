    private ArrayList<LBE_ParametroValor[]> elementos = new ArrayList<LBE_ParametroValor[]>();

    private void cargaDatos() throws LBU_Exception {
        LBU_Parametro buParam = new LBU_Parametro(Constantes.UsuarioLogin.getBDatos());
        LBE_Parametro parametro = buParam.getListaValores(idQuery, nomParam);
        if (parametro == null) {
            this.cancelado = true;
            this.setVisible(false);
            return;
        }
        ResultSet rs = parametro.getResultSet();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int numRowSel = -1;
        int i = 0;
        try {
            rs.setFetchSize(10000);
            while (rs.next()) {
                LBE_ParametroValor paramValor = new LBE_ParametroValor();
                LBE_ParametroValor[] datos = {paramValor};
                elementos.add(datos);
                paramValor.setValor(rs.getString(1));
                paramValor.setDescripcion(rs.getString(2));
                if (paramValor.getValor().trim().equalsIgnoreCase(valParam)) {
                    numRowSel = i;
                }
                i++;
                model.addRow(datos);
            }
        } catch (SQLException ex) {
            Log.error("Error al leer lista de par치metros", ex);
            throw new LBU_Exception("Error al leer lista de parametros");
        } catch (NullPointerException ex) {
            Log.error("Error al leer lista de par치metros", ex);
            throw new LBU_Exception("Error al leer lista de parametros");
        }
        if (numRowSel != -1) {
            jTable1.setRowSelectionInterval(numRowSel, numRowSel);
        }
    }


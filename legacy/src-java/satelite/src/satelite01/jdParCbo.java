    private void jtfBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfBuscarKeyReleased
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        int numRowSel = -1;
        int i = 0;
        try {
            if (!jtfBuscar.getText().trim().equals("")) {
                model.setNumRows(0);
                for (LBE_ParametroValor[] elem : elementos) {
                    if (elem[0].getDescripcion().toUpperCase().startsWith(jtfBuscar.getText().toUpperCase())) {
                        if (elem[0].getValor().trim().equalsIgnoreCase(valParam)) {
                            numRowSel = i;
                        }
                        i++;
                        model.addRow(elem);
                    }
                }
            } else {
                model.setNumRows(0);
                for (LBE_ParametroValor[] elem : elementos) {
                    if (elem[0].getValor().trim().equalsIgnoreCase(valParam)) {
                        numRowSel = i;
                    }
                    i++;
                    model.addRow(elem);
                }
            }
        } catch (NullPointerException ex) {
            Log.error("Error al leer lista de par치metros", ex);
            //throw new BU_Exception("Error al leer lista de parametros");
        }
        if (numRowSel != -1) {
            jTable1.setRowSelectionInterval(numRowSel, numRowSel);
        }
    }//GEN-LAST:event_jtfBuscarKeyReleased

    private void validaDatos() throws LBU_Exception {
        try {
            //jSpinner1.getModel().;
            jSpinner1.commitEdit();
            jtfNombre.grabFocus();
            if (jtfNombre.getText().trim().equals("")) {
                throw new LBU_Exception("Debe ingresar un valor para el nombre del grupo");
            }
            if (jtfNombre.getText().length() > 35) {
                throw new LBU_Exception("Tamaño de nombre no debe exceder los 35 caractéres");
            }
            Integer tagOpcSeg = (Integer) jSpinner1.getValue();
            if (tagOpcSeg <= 0) {
                throw new LBU_Exception("Codigo de Opción de Seguridad debe ser mayor a 0");
            }
        } catch (ParseException ex) {
            throw new LBU_Exception("Código de Opción de Seguridad incorrecto");
        }

    }

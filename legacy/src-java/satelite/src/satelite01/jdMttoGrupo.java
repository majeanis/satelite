    private void jbSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSaveActionPerformed
        // TODO add your handling code here:
        try {
            validaDatos();
            LBU_Grupo objBU = new LBU_Grupo(Constantes.UsuarioLogin.getBDatos());

            LBE_Grupo elem = new LBE_Grupo();
            elem.setId(id);
            elem.setNombre(jtfNombre.getText());
            elem.setIdOpcionSegur((Integer) jSpinner1.getValue());
            if (this.id == 0) {
                //Crea el grupo
                objBU.add(elem, Constantes.UsuarioLogin.getUsrLogin());
            } else {
                //Guarda el grupo
                objBU.save(elem, Constantes.UsuarioLogin.getUsrLogin());
            }
            cancelar=false;
            setVisible(false);

        } catch (LBU_Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Error al crear/guardar grupo: "
                    + ex.getMessage(), "Validación", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbSaveActionPerformed


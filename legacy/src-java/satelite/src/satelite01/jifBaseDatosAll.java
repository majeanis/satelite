    private void editar()
    {
        int numRow = jTable1.getSelectedRow();
        if(numRow>-1)
        {
            Integer id = (Integer) jTable1.getValueAt(numRow, 0);
            jdMttoBDatos mttoBD = new jdMttoBDatos(null, true, id);
            mttoBD.setLocationRelativeTo(this);
            mttoBD.setVisible(true);
            if(!mttoBD.isCancelar())
                cargar();
        }else
        {
            JOptionPane.showMessageDialog(rootPane,
                    "Debe seleccionar una registro","Base de Datos",
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }

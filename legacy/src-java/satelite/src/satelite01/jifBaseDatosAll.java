    private void cargar() {
        LBU_BDatos objBU;
        try {
            objBU = new LBU_BDatos(Constantes.UsuarioLogin.getBDatos());
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            //model.getDataVector().removeAllElements();
            int numRows = model.getRowCount();
            for (int i = numRows - 1; i >= 0; i--) {
                model.removeRow(i);
            }
            for (LBE_BDatos elem : objBU.getAll()) {
                Vector row = new Vector();
                row.add(elem.getCodigo());
                row.add(elem.getNombre());
                row.add(elem.getUrl());
                row.add(elem.getUsuario());
                row.add(elem.getDriver().getNombre());
                model.addRow(row);
            }
        } catch (LBU_Exception ex) {
            Log.error("Error al cargar lista de Base de Datos", ex);
        }
    }


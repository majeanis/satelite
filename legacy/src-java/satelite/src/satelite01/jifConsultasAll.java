    private void cargaGrupos(DefaultMutableTreeNode root) throws LBU_Exception {
        jbEditGrup.setEnabled(false);
        //jpMain.set;
        root.removeAllChildren();
        DefaultTreeModel treeModel = (DefaultTreeModel) jTree1.getModel();
        LBU_Grupo objBUS = new LBU_Grupo(Constantes.UsuarioLogin.getBDatos());
        ArrayList<LBE_Grupo> grupos = objBUS.getAll();
        for (LBE_Grupo grupo : grupos) {
            DefaultMutableTreeNode nodeGrupo = new DefaultMutableTreeNode(grupo);
            root.add(nodeGrupo);
        }
        treeModel.reload();
        cargaBDatos();
    }

    private void cargaGrupos() {
        try {
            cargaGrupos(root);
        } catch (LBU_Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Error al cargar datos: "
                    + ex.getMessage(), "Error", JOptionPane.WARNING_MESSAGE);

        }
    }


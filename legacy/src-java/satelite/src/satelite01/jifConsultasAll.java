    private void jbNewConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbNewConsultaActionPerformed
        // TODO add your handling code here:
        TreePath selPath = jTree1.getSelectionPath();
        int idGrupo=0;
        if (selPath == null) {
            idGrupo=0;
        } else {
            DefaultMutableTreeNode nodeSel = (DefaultMutableTreeNode) selPath.getLastPathComponent();
            if (nodeSel == null) {
                idGrupo=0;
            } else if (!nodeSel.isRoot()) {
                LBE_Grupo grupo = (LBE_Grupo) nodeSel.getUserObject();
                idGrupo=grupo.getId();
            } else {
                idGrupo=0;
            }
        }

        jdMttoConsulta frmCons = new jdMttoConsulta(null, true, 0,idGrupo);
        frmCons.setLocationRelativeTo(this);
        frmCons.setVisible(true);
        if (!frmCons.isCancelar()) {
            try {
                cargaBDatos();
            } catch (LBU_Exception ex) {
                JOptionPane.showMessageDialog(frmCons, "Error al cargar BDatos ",
                        "Error", JOptionPane.WARNING_MESSAGE);
            }
        }


    }//GEN-LAST:event_jbNewConsultaActionPerformed
    private void editar() {
        int numRow = jTable1.getSelectedRow();
        if (numRow > -1) {
            Integer id = (Integer) jTable1.getValueAt(numRow, 0);
            jdMttoConsulta frmCons = new jdMttoConsulta(null, true, id);
            frmCons.setLocationRelativeTo(this);
            frmCons.setVisible(true);
            if (!frmCons.isCancelar()) {
                try {
                    cargaBDatos();
                } catch (LBU_Exception ex) {
                    JOptionPane.showMessageDialog(frmCons, "Error al cargar consulta ",
                            "Error", JOptionPane.WARNING_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane,
                    "Debe seleccionar una registro", "Consulta",
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }


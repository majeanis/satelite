    private void cargaDatos()
    {
        long memoryMax = Runtime.getRuntime().maxMemory() ;
        long memoryTot = Runtime.getRuntime().totalMemory();
        long memoryFre = Runtime.getRuntime().freeMemory();
        DecimalFormat df = new DecimalFormat("0.00");
        jtfMemMax.setText(df.format(memoryMax /1000000F) + " MB");
        jtfMemTot.setText(df.format(memoryTot/1000000F) + " MB");
        jtfMemLib.setText(df.format(memoryFre/1000000F) + " MB");        
    }

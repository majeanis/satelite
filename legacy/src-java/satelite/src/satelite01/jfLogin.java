    class Task extends SwingWorker<Void, Void> {
        /*
         * Main task. Executed in background thread.
         */

        jfLogin frame;

        public Task(jfLogin frame) {
            this.frame = frame;
        }

        @Override
        public Void doInBackground() {
            jProgressBar1.setVisible(true);
            setProgress(1);
            try {
                LBU_Seguridad objBU = new LBU_Seguridad();
                String user = jtfUser.getText();
                char[] paswC = jpfPasw.getPassword();
                StringBuilder sbPasw = new StringBuilder();
                for (int ix = 0; ix < paswC.length; ix++) {
                    sbPasw.append(paswC[ix]);
                }
                jbIngresar.setEnabled(false);
                jtfUser.setEnabled(false);
                jpfPasw.setEnabled(false);
                Log.debug("Validacion de Login");
                Constantes.UsuarioLogin = objBU.login(user, sbPasw.toString());
                Log.debug("Login Ok");
                Constantes.propiedades.setProperty(Constantes.cPROP_USR_LOG, jtfUser.getText());
                jProgressBar1.setVisible(false);
                this.frame.dispose();
            } catch (LBU_Exception ex) {
                Log.debug("Error de login: " + ex.getMessage());
                JOptionPane.showMessageDialog(this.frame, ex.getOraMessage());
                //JOptionPane.showMessageDialog(this.frame, ex.getMessage());
                jbIngresar.setEnabled(true);
                jtfUser.setEnabled(true);
                jpfPasw.setEnabled(true);
            } catch (LBU_ClaveCaducoException ex) {
                Log.debug("Error de login: " + ex.getMessage());
                jProgressBar1.setVisible(false);
                jdCambiaPassword fCP = new jdCambiaPassword(mdiMain, true, jtfUser.getText(), true);
                fCP.setLocationRelativeTo(mdiMain);
                fCP.setVisible(true);
                if (!fCP.esCancelar()) {
                    this.frame.setVisible(false);
                } else {
                    jbIngresar.setEnabled(true);
                    jtfUser.setEnabled(true);
                    jpfPasw.setEnabled(true);
                }
            } catch (Exception ex) {
                Log.error("Error en login: " + ex.getMessage(),ex );
            }
            setProgress(100);

            return null;
        }

        /*
         * Executed in event dispatching thread
         */
        @Override
        public void done() {
            //Toolkit.getDefaultToolkit().beep();
            jbIngresar.setEnabled(true);
            setCursor(null);
            jProgressBar1.setIndeterminate(false);
            jProgressBar1.setString("");
            jProgressBar1.setVisible(false);
        }
    }

    public void propertyChange(PropertyChangeEvent evt) {
        if ("progress".equals(evt.getPropertyName())) {
            jProgressBar1.setIndeterminate(true);
            jProgressBar1.setString("Validando Usuario");
        }
    }

    private void cargaBDatos(LBE_Consulta consulta) throws LBU_Exception {
        DefaultComboBoxModel model = (DefaultComboBoxModel) jcbBDatos.getModel();
        model.removeAllElements();
        LBU_BDatos objBU = new LBU_BDatos(Constantes.UsuarioLogin.getBDatos());
        for (LBE_BDatos elem : objBU.getAll()) {
            model.addElement(elem);
            if (consulta != null) {
                if (elem.getCodigo() == consulta.getBe_BDatos().getCodigo()) {
                    model.setSelectedItem(elem);
                }
            }
        }
    }

    private void cargaGrupos(LBE_Consulta consulta) throws LBU_Exception {
        DefaultComboBoxModel model = (DefaultComboBoxModel) jcbGrupos.getModel();
        model.removeAllElements();
        LBU_Grupo objBU = new LBU_Grupo(Constantes.UsuarioLogin.getBDatos());
        for (LBE_Grupo elem : objBU.getAll()) {
            model.addElement(elem);
            if (consulta != null) {
                if (elem.getId() == consulta.getGrupo().getId()) {
                    model.setSelectedItem(elem);
                }
            }else
            {
                if (elem.getId() == this.idGrupo) {
                    model.setSelectedItem(elem);
                }
            }
        }
    }

    private LBE_Consulta cargaDatos() throws LBU_Exception {
        LBU_Consulta objBU = new LBU_Consulta(Constantes.UsuarioLogin.getBDatos());
        LBE_Consulta consulta = objBU.get(codigo);
        jtfTitulo.setText(consulta.getTitulo());
        jSpinner1.setValue(consulta.getIdOpcionSegur());
        jtaConsulta.setText(consulta.getQuery());
        return consulta;
    }

    private void jbSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSaveActionPerformed
        try {
            // TODO add your handling code here:
            jSpinner1.commitEdit();
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(rootPane, "Error en formato de Id de OpciÃ³n de Seguridad");
            return;
        }
        LBE_Consulta elem = new LBE_Consulta();
        elem.setTitulo(jtfTitulo.getText());
        elem.setIdOpcionSegur((Integer)jSpinner1.getValue());
        elem.setBe_BDatos((LBE_BDatos) jcbBDatos.getSelectedItem());
        elem.setGrupo((LBE_Grupo) jcbGrupos.getSelectedItem());
        elem.setQuery(jtaConsulta.getText());

        try
        {
            LBU_Consulta objBU = new LBU_Consulta(Constantes.UsuarioLogin.getBDatos());
            if (this.codigo > 0) {
                elem.setId(this.codigo);
                objBU.save(elem, Constantes.UsuarioLogin.getUsrLogin());
            } else {
                objBU.add(elem, Constantes.UsuarioLogin.getUsrLogin());
            }
        }catch(LBU_Exception ex)
        {
            JOptionPane.showMessageDialog(rootPane, 
                    "Error al crear/guardar consulta:" + ex.getMessage());
            return;
        }
        cancelar = false;
        setVisible(false);
    }//GEN-LAST:event_jbSaveActionPerformed

    private void jbValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbValidarActionPerformed
        // TODO add your handling code here:

        LBE_Consulta consulta = new LBE_Consulta();
        consulta.setBe_BDatos((LBE_BDatos) jcbBDatos.getSelectedItem());
        consulta.setQuery(jtaConsulta.getText());
        String query = consulta.getQuery();
        try {
            ArrayList<String> params = consulta.parametros();
            if (params.size() > 0) {
                jdParametros frmParametros =
                        new jdParametros(
                        null,
                        true,
                        params,
                        consulta.getId());
                frmParametros.setLocationRelativeTo(this);
                frmParametros.setVisible(true);
                if (!frmParametros.isCancelado()) {
                    JOptionPane.showMessageDialog(null, "Consulta cancelada");
                    return;
                }
                DefaultTableModel model = frmParametros.getModel();
                for (int ix = 0; ix < model.getRowCount(); ix++) {
                    String nomParam = (String) model.getValueAt(ix, 0);
                    String valParam = (String) model.getValueAt(ix, 1);
                    query = query.replace("@" + nomParam + "@", valParam);
                    Constantes.propiedades.setProperty("param." + nomParam, valParam);
                }
                frmParametros.dispose();
            }
            LBU_Consulta objBU = new LBU_Consulta(Constantes.UsuarioLogin.getBDatos());
            ResultSet rs = objBU.ejecutar(consulta, query, "Test", true);

            JOptionPane.showMessageDialog(null, "Test OK");

        } catch (LBE_Exception ex) {
            JOptionPane.showMessageDialog(null, "Error:" + ex.getMessage());
        } catch (LBU_Exception ex) {
            JOptionPane.showMessageDialog(null, "Error:" + ex.getMessage());
        }
    }//GEN-LAST:event_jbValidarActionPerformed

